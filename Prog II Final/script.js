var side = 10;
let grassArr = []
let grassEaterArr = []
let predatorArr = []
let killerArr = []
let stoneArr = []

let matrix = []
function matrixGenerator(size, countGrass, countGrassEater, predatorCount, killerCount, StoneCount) {
    for (let i = 0; i < size; i++) {
        matrix.push([])
        for (let j = 0; j < size; j++) {
            matrix[i].push(0)
        }

    }
    for (let k = 0; k < countGrass; k++) {
        let x = Math.floor(random(size))
        let y = Math.floor(random(size))
        if (matrix[y][x] == 0) {
            matrix[y][x] = 1
        }
        else {
            k--
        }


    }
    for (let k = 0; k < countGrassEater; k++) {
        let x = Math.floor(random(size))
        let y = Math.floor(random(size))
        if (matrix[y][x] == 0) {
            matrix[y][x] = 2
        }
        else {
            k--
        }
    }
    for (let k = 0; k < predatorCount; k++) {
        let x = Math.floor(random(size))
        let y = Math.floor(random(size))
        if (matrix[y][x] == 0) {
            matrix[y][x] = 3
        }
        else {
            k--
        }
    }
    for (let k = 0; k < killerCount; k++) {
        let x = Math.floor(random(size))
        let y = Math.floor(random(size))
        if (matrix[y][x] == 0) {
            matrix[y][x] = 4
        }
        else {
            k--
        }
    }
    for (let k = 0; k < StoneCount; k++) {
        let x = Math.floor(random(size))
        let y = Math.floor(random(size))
        if (matrix[y][x] == 0) {
            matrix[y][x] = 5
        }
        else {
            k--
        }
    }
}





function setup() {
    frameRate(10);
    matrixGenerator(100, 250, 15, 15, 15, 50)
    background('#acacac');
    // for (var y = 0; y < n; y++) {
    //     matrix[y ] = []
    // for (var x = 0; x < m; x++) {
    //     matrix[y].push(Math.round(random(0, 1)))
    // }
    // }
    createCanvas(matrix[0].length * side, matrix.length * side);
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix.length; x++) {
            if (matrix[y][x] == 1) {
                var grass = new Grass(x, y);
                grassArr.push(grass)
            }
            else if (matrix[y][x] == 2) {
                var grassEat = new GrassEater(x, y);
                grassEaterArr.push(grassEat)
            }
            else if (matrix[y][x] == 3) {
                var predator = new Predator(x, y);
                predatorArr.push(predator)
            }
            else if (matrix[y][x] == 4) {
                var killer = new Killer(x, y);
                killerArr.push(killer)
            }
            else if (matrix[y][x] == 5) {
                var stone = new Stone(x, y);
                stoneArr.push(stone)
            }
        }
    }
}




